package com.example.fromtoday;

public class walk {
    String walk_Week;
    String walk_distance;
    String walk_kcal;
    String walk_speed;
    String walk_time;

    public String getWalk_Week() {
        return walk_Week;
    }

    public void setWalk_Week(String walk_Week) {
        this.walk_Week = walk_Week;
    }

    public String getWalk_distance() {
        return walk_distance;
    }

    public void setWalk_distance(String walk_distance) {
        this.walk_distance = walk_distance;
    }

    public String getWalk_kcal() {
        return walk_kcal;
    }

    public void setWalk_kcal(String walk_kcal) {
        this.walk_kcal = walk_kcal;
    }

    public String getWalk_speed() {
        return walk_speed;
    }

    public void setWalk_speed(String walk_speed) {
        this.walk_speed = walk_speed;
    }

    public String getWalk_time() {
        return walk_time;
    }

    public void setWalk_time(String walk_time) {
        this.walk_time = walk_time;
    }
}
